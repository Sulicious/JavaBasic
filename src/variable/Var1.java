// 패키지는 자바 파일을 구분하기 위한 폴더

/* 패키지를 만들었다면 해당 패키지에 들어가는 클래스는
 아래와 같이 소속된 패키지를 선언해야한다.
*/
package variable;

// 클래스를 만들 때는 관례상으로 파일의 첫 문자를 대문자로 한다.
public class Var1 {
    public static void main(String[] args) {
        System.out.println(20); // 변경 10 -> 20
        System.out.println(20); // 변경 10 -> 20
        System.out.println(20); // 변경 10 -> 20
    }
}
